package com.example.foodapp.slides;

public class HINH_SLIDES {
    private String url;
    private String link;
    
    public HINH_SLIDES(String url, String link) {
        this.url = url;
        this.link = link;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}

